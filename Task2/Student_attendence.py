def make_table(Data):
    names = sorted({name for name, _, _ in Data})
    dates = sorted({date for _, date, _ in Data})
    
    rows = len(names)
    cols = len(dates)
    
    table = [["NA"] * cols for _ in range(rows)]
    
    for i in range(rows):
        for j in range(cols):
            for name, date, status in Data:
                if name == names[i] and date == dates[j]:
                    table[i][j] = "P" if status == "Present" else "A"
                    
    return names, dates, table

def get_percentage(table, dates, names):
    attendance_percentage = {}

    total_days = len(dates)

    for i in range(len(names)):
        present_count = 0

        for j in range(len(dates)):
            if table[i][j] == "P":
                present_count += 1

        percentage = (present_count / total_days) * 100
        attendance_percentage[names[i]] = percentage

    return attendance_percentage

def get_max_streak(table, dates, names):
    
    max_streak = {}
    
    for i in range(len(names)):
        
        streak = 0
        cur_streak = 0
        for j in range(len(dates)):
            if table[i][j] == "P":
                cur_streak = cur_streak + 1
            else:
                cur_streak = 0
            
            streak = max(streak, cur_streak)
        max_streak[names[i]] = streak
    
    return max_streak

def analyse(Data):
    names, dates, table = make_table(Data)
    attendence_percentage = get_percentage(table, dates, names)
    max_streak = get_max_streak(table, dates, names)
    
    report = {}

    for name in names:
        report[name] = {
            "attendance_percentage": round(attendence_percentage[name], 2),
            "max_present_streak": max_streak[name]
        }
    
    return report

result = analyse(Data)

